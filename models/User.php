            [['role_id'], 'integer'],
            [['login'], 'string', 'max'=>10, 'min'=>3, 'tooLong' => '12313', 'tooShort' => 'Toooo Shoooort','message'=>'123'],
            [['phone', 'email', 'login', 'password'], 'required'],
